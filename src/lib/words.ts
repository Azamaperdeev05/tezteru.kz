export const DEFAULT_WORD_CATEGORIES = WORD_CATEGORIES.map((category) => category.id);

export const KAZAKH_WORDS = Array.from(
  new Set(WORD_CATEGORIES.flatMap((category) => category.words))
);

export function getWordPool(selectedCategories?: WordCategoryId[]): string[] {
  if (!selectedCategories || selectedCategories.length === 0) {
    return KAZAKH_WORDS;
  }

  const pool = WORD_CATEGORIES
    .filter((category) => selectedCategories.includes(category.id))
    .flatMap((category) => category.words);

  return pool.length > 0 ? Array.from(new Set(pool)) : KAZAKH_WORDS;
}

export function generateWords(
  amount: number,
  punctuation: boolean,
  numbers: boolean,
  practiceWords: string[] = [],
  selectedCategories?: WordCategoryId[]
): string {
  let words: string[] = [];

  if (practiceWords && practiceWords.length > 0) {
    while (words.length < amount) {
      words.push(practiceWords[Math.floor(Math.random() * practiceWords.length)]);
    }
  } else {
    const pool = getWordPool(selectedCategories);

    for (let i = 0; i < amount; i++) {
      words.push(pool[Math.floor(Math.random() * pool.length)]);
    }
  }

  if (punctuation) {
    words = words.map((value, index) => {
      let word = value;

      if (Math.random() < 0.2) {
        word = word.charAt(0).toUpperCase() + word.slice(1);
      }

      if (Math.random() < 0.15 && index !== words.length - 1) {
        word += ',';
      } else if (Math.random() < 0.1 && index !== words.length - 1) {
        word += '.';
      } else if (Math.random() < 0.05 && index !== words.length - 1) {
        word += '?';
      }

      return word;
    });
  }

  if (numbers) {
    words = words.map((word) => {
      if (Math.random() < 0.1) {
        return Math.floor(Math.random() * 1000).toString();
      }

      return word;
    });
  }

  return words.join(' ');
}
