export function generateWords(
  amount: number,
  punctuation: boolean,
  numbers: boolean,
  practiceWords: string[] = []
): string {
  let words: string[] = [];
  
  if (practiceWords && practiceWords.length > 0) {
    // Fill with practice words
    while (words.length < amount) {
      words.push(practiceWords[Math.floor(Math.random() * practiceWords.length)]);
    }
  } else {
    for (let i = 0; i < amount; i++) {
      words.push(KAZAKH_WORDS[Math.floor(Math.random() * KAZAKH_WORDS.length)]);
    }
  }

  if (punctuation) {
    words = words.map((w, i) => {
      let word = w;
      if (Math.random() < 0.2) word = word.charAt(0).toUpperCase() + word.slice(1);
      if (Math.random() < 0.15 && i !== words.length - 1) word += ',';
      else if (Math.random() < 0.1 && i !== words.length - 1) word += '.';
      else if (Math.random() < 0.05 && i !== words.length - 1) word += '?';
      return word;
    });
  }

  if (numbers) {
    words = words.map(w => {
      if (Math.random() < 0.1) {
        return Math.floor(Math.random() * 1000).toString();
      }
      return w;
    });
  }

  return words.join(' ');
}
